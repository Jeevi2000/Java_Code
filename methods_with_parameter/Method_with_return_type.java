package com.java.methods_with_parameter;

public class Method_with_return_type {
	
	
	public static void main(String[] args)
	{
		
//		System.out.println(add());
		int f=add();
		System.out.println(f);
		
	}
	
	public static int add()
	{
		
		int a=12;
		int b=13;
		int c=a+b;
		System.out.println(c);
	return c;
	}
}
