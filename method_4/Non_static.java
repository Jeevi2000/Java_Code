package com.java.method_4;

public class Non_static {
	
	
	public static void main(String[] args) {
		Non_static obj=new Non_static();
		obj.car();
		obj.house();
		
	}
	
	public void car()
	{
		System.out.println("i have 2 cars");
		System.out.println(" i have 3 cars");
	}
	public void house()
	{
		System.out.println("i have 2 house");
	}


}
