package com.java.loopStatement;

public class Do_while {

	public static void main(String[] args)
	{
//		The Java do-while loop is executed at least once because condition is checked after loop body.
		
	int i = 1;
	do
	{
		System.out.println(i);
		i++;
	}
	while(i<=3);
		
	}


}
