package com.java.operator;

public class relational {
	public static void main(String[] args) {
		
		int g=27,m=37;
		System.out.println(g==m);
		System.out.println(g>m);
		System.out.println(m>g);
		System.out.println(g!=m);
		System.out.println(g>=m);
		System.out.println(g<=m);
	}

}
