package com.java.Incapsulation_facebook;

public class Facebook 
{
  private int post;
  private int followers;
  private int following;
  private int friends;
protected int getPost() {
	return post;
}
protected void setPost(int post) {
	this.post = post;
}
protected int getFollowers() {
	return followers;
}
protected void setFollowers(int followers) {
	this.followers = followers;
}
protected int getFollowing() {
	return following;
}
protected void setFollowing(int following) {
	this.following = following;
}
protected int getFriends() {
	return friends;
}
protected void setFriends(int friends) {
	this.friends = friends;
}
  
  

}
